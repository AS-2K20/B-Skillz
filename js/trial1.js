function category(a,b){
    var tQA = a;
    var tCA = b;

    if((tQA>50)&&(tCA<50)){
        cate = "hybrid";
    }

    else if((tQA>0) && (tQA<=28)&& (tCA>0) && (tCA<=28)){
        cate = "seed";
    }

    else if((tQA>=29) && (tQA<=40) && (tCA>=29) && (tCA<=40)){
        cate ="plant";
    }

    else if((tQA>=41) && (tCA>=41))
    {
        cate = "tree";
    }
    else if((tQA==0) && (tCA==0)){
        cate = "zero";
    }
    else {
        cate = "hybrid";
    }
}
